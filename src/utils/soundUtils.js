// Sound utility for website interactions
class SoundManager {
  constructor() {
    this.sounds = {};
    this.backgroundMusic = null;
    this.enabled = localStorage.getItem('soundEnabled') !== 'false';
    this.musicEnabled = localStorage.getItem('musicEnabled') !== 'false';
  }

  // Load a sound
  loadSound(name, src, volume = 0.5) {
    try {
      const audio = new Audio(src);
      audio.volume = volume;
      audio.preload = 'auto';
      this.sounds[name] = audio;
    } catch (error) {
      console.warn(`Failed to load sound: ${name}`, error);
    }
  }

  // Play a sound
  play(name) {
    if (!this.enabled || !this.sounds[name]) return;
    
    try {
      const audio = this.sounds[name];
      audio.currentTime = 0; // Reset to start
      audio.play().catch(e => console.warn(`Sound play failed: ${name}`, e));
    } catch (error) {
      console.warn(`Error playing sound: ${name}`, error);
    }
  }

  // Toggle sound on/off
  toggle() {
    this.enabled = !this.enabled;
    localStorage.setItem('soundEnabled', this.enabled);
    return this.enabled;
  }

  // Load background music
  loadBackgroundMusic(src, volume = 0.3) {
    try {
      this.backgroundMusic = new Audio(src);
      this.backgroundMusic.volume = volume;
      this.backgroundMusic.loop = true;
      this.backgroundMusic.preload = 'auto';
    } catch (error) {
      console.warn('Failed to load background music', error);
    }
  }

  // Play background music
  playBackgroundMusic() {
    if (!this.musicEnabled || !this.backgroundMusic) return;
    
    try {
      this.backgroundMusic.play().catch(e => console.warn('Background music play failed', e));
    } catch (error) {
      console.warn('Error playing background music', error);
    }
  }

  // Stop background music
  stopBackgroundMusic() {
    if (this.backgroundMusic) {
      this.backgroundMusic.pause();
      this.backgroundMusic.currentTime = 0;
    }
  }

  // Toggle music on/off
  toggleMusic() {
    this.musicEnabled = !this.musicEnabled;
    localStorage.setItem('musicEnabled', this.musicEnabled);
    
    if (this.musicEnabled) {
      this.playBackgroundMusic();
    } else {
      this.stopBackgroundMusic();
    }
    
    return this.musicEnabled;
  }

  // Set volume for all sounds
  setVolume(volume) {
    Object.values(this.sounds).forEach(audio => {
      audio.volume = Math.max(0, Math.min(1, volume));
    });
  }
}

